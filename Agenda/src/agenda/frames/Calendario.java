    private void atualizaCalendario(int mes, int ano){
        
        LocalDate hoje = LocalDate.now();
        
        if (mes==13){
            mes = 1;
            ano += 1;
        } else if (mes==0){
            mes = 12;
            ano -= 1;
        }
        
        int primeiro_dia = LocalDate.of(ano, mes, 1).getDayOfWeek().getValue();
        int ultimo_dia = LocalDate.of(ano, mes, 1).lengthOfMonth();
        
        String[] meses = {"Janeiro", "Fevereiro", "Março", "Abril",
                            "Maio", "Junho","Julho","Agosto",
                            "Setembro","Outubro", "Novembro", "Dezembro"};
        
        javax.swing.JLabel[][] lb_dias = {
                {lb_11, lb_12, lb_13, lb_14, lb_15, lb_16, lb_17,},
                {lb_21, lb_22, lb_23, lb_24, lb_25, lb_26, lb_27},
                {lb_31, lb_32, lb_33, lb_34, lb_35, lb_36, lb_37},
                {lb_41, lb_42, lb_43, lb_44, lb_45, lb_46, lb_47},
                {lb_51, lb_52, lb_53, lb_54, lb_55, lb_56, lb_57},
                {lb_61, lb_62, lb_63, lb_64, lb_65, lb_66, lb_67}};
        
        lbAno.setText(Integer.toString(ano));
        lbMes.setText(meses[mes-1]);
        
        Border empty_border = BorderFactory.createEmptyBorder();
        Border black_border = BorderFactory.createLineBorder(Color.black);
        Border red_border = BorderFactory.createLineBorder(Color.red, 3);
        
        Cursor hand_cursor = new Cursor(Cursor.HAND_CURSOR);
        Cursor default_cursor = new Cursor(Cursor.DEFAULT_CURSOR);
        
        SQLiteDataBase DB = new SQLiteDataBase();

        String data;
        String s_dia;
        int eventos_dia;
        int dia = 1;
        for(int i=0;i<=5; i++){
            for(int j=0;j<=6; j++){
                
                s_dia = Integer.toString(dia);
                if (s_dia.length()<2){ s_dia = "0"+s_dia; }
                
                data = Integer.toString(ano) + "-" + Integer.toString(mes) + "-" + s_dia;
                eventos_dia = DB.getEventosData(this.db_path, this.agenda_id, data).size();
                
                lb_dias[i][j].setBorder(empty_border);
                lb_dias[i][j].setText("");
                lb_dias[i][j].setCursor(default_cursor);
                lb_dias[i][j].setForeground(Color.black);
                
                if(i==0 && j>=(primeiro_dia-1)){
                    lb_dias[i][j].setText(Integer.toString(dia));
                    lb_dias[i][j].setCursor(hand_cursor);
                    if (LocalDate.of(ano, mes, dia).equals(hoje)){
                        lb_dias[i][j].setBorder(red_border);
                    } else {
                        lb_dias[i][j].setBorder(black_border);
                    }
                    if (eventos_dia>0){
                        lb_dias[i][j].setForeground(Color.blue);
                    }
                    dia++;
                } else if(i>0 && dia<=ultimo_dia){
                    lb_dias[i][j].setText(Integer.toString(dia));
                    lb_dias[i][j].setCursor(hand_cursor);
                    if (LocalDate.of(ano, mes, dia).equals(hoje)){
                        lb_dias[i][j].setBorder(red_border);
                    } else {
                        lb_dias[i][j].setBorder(black_border);
                    }
                    if (eventos_dia>0){
                        lb_dias[i][j].setForeground(Color.blue);
                    }
                    dia++;
                }
            }
        }
    }
    
