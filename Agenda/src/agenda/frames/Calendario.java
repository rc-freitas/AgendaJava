    private void loadEventosDialog(int dia){
        
        int ano = this.getAno();
        int mes = this.getMes();
        
        String s_dia = Integer.toString(dia);
        if (dia<10){ s_dia = "0" + s_dia; }
        
        String data_completa = s_dia + "/" + mes + "/" + ano;
        String titulo = "Eventos - " + s_dia + "/" + mes + "/" + ano;
        lb_dia.setText(data_completa);
        jD_Eventos.setTitle(titulo);
        
        String data = ano + "-" + mes + "-" + s_dia;

        SQLiteDataBase DataBase = new SQLiteDataBase();
        if (!this.eventos_dia.isEmpty()){ this.eventos_dia.clear();}
        this.eventos_dia = DataBase.getEventosData(this.db_path, this.agenda_id, data);
        
        DefaultListModel listModelHorarios = new DefaultListModel();
        String h;
       
        listModelHorarios.clear();
        jL_ListaEventos.setModel(listModelHorarios);
        if (this.eventos_dia.size()>0){
            for (int i=0; i<this.eventos_dia.size(); i++){
                h = this.eventos_dia.get(i).getHoraInicial() + " - " + this.eventos_dia.get(i).getHoraFinal();
                listModelHorarios.add(i, h);
            }
            jL_ListaEventos.setModel(listModelHorarios);
        }
        
        jD_Eventos.setVisible(true);
    }
    
