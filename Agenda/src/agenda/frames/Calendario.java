    private void jBtn_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_SalvarActionPerformed
        // TODO add your handling code here:
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd/MM/yyyy");
        
        String nome = jTF_nome.getText();
        String descricao = jTA_descricao.getText();
        String data = LocalDate.parse(jTF_data.getText(), formatter).toString();
        String hora = jTF_hora_inicial.getText();
        String hora_final = jTF_hora_final.getText();
        boolean dia_inteiro = jCB_DiaInteiro.isSelected();
        try {
            Evento novo_evento = new Evento(this.evento_id, nome, descricao, dia_inteiro, data, hora, hora_final);
            SQLiteDataBase DB = new SQLiteDataBase();
            DB.saveEvento(this.db_path, novo_evento, this.agenda_id);
            
            this.loadEventosDialog(this.dia_aberto);
            
            this.evento_id=0;
            jTF_nome.setText("");
            jTA_descricao.setText("");
            jTF_data.setText("");
            jTF_hora_inicial.setText("");
            jTF_hora_final.setText("");
            jCB_DiaInteiro.setSelected(false);
        } catch (Exception erro_message){
            JOptionPane.showMessageDialog(this, erro_message, "Erro",
                                            JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jBtn_SalvarActionPerformed

    private void jBtn_apagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_apagarActionPerformed
        // TODO add your handling code here:
        if(this.evento_id!=0){
            Object[] opc = { "CONFIRMAR", "Cancelar" };
            int resposta = JOptionPane.showOptionDialog(this, "Tem certeza que "
                    + "deseja apagar o evento " + jTF_nome.getText() + "?", 
                    "Apagar evento", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, opc, opc[0]);
            if(resposta==0){
                 try {
                    SQLiteDataBase DB = new SQLiteDataBase();
                    DB.deleteEvento(this.db_path, this.evento_id);
                    this.loadEventosDialog(this.dia_aberto);
                    this.evento_id=0;
                    jTF_nome.setText("");
                    jTA_descricao.setText("");
                    jTF_data.setText("");
                    jTF_hora_inicial.setText("");
                    jTF_hora_final.setText("");
                    jCB_DiaInteiro.setSelected(false);
                } catch (Exception erro_message){
                    JOptionPane.showMessageDialog(this, erro_message, "Erro",
                                                    JOptionPane.ERROR_MESSAGE);
                }   
            }
            
        }
    }//GEN-LAST:event_jBtn_apagarActionPerformed

